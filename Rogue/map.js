function mapCollide() {
    let nextX = character.x; // Default to current position
    let nextY = character.y;

    // Check if character is moving and predict the next position
    if (character.dx !== 0) {
        nextX += character.dx; 
    }
    if (character.dy !== 0) {
        nextY += character.dy; 
    }

    // Convert to tile index
    let tileX = Math.floor(nextX / TILE_SIZE);
    let tileY = Math.floor(nextY / TILE_SIZE);
    let tileIndex = tileY * MAP_WIDTH + tileX;

    if (solid.includes(currentMap[tileIndex])) {
        console.log("Collision detected!"); // Debugging message
        character.x = character.prevX; // Reset to last valid position
        character.y = character.prevY;
    } else {
        character.prevX = character.x; // Update previous valid position
        character.prevY = character.y;
    }
}

